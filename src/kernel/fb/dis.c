int curLine = 0;  // global current line position
int curX = 0;

void draw_char(uint32_t *fb, int fb_width, int x, int y, char c, uint32_t color) {
    if (c < 32 || c > 127) return;
    const uint8_t *bitmap = font8x8_basic[(int)c];

    for (int row = 0; row < CHAR_H; row++) {
        uint8_t bits = bitmap[row];
        for (int col = 0; col < CHAR_W; col++) {
            if (bits & (1 << col)) {
                fb[(y + row) * fb_pitch_pixels + (x + col)] = color;
            }
        }
    }
    curX += CHAR_W;
}

void draw_string(uint32_t *fb, int fb_width, int x, int y, const char *s, uint32_t color) {
    int cur_x = x;
    int cur_y = curLine;  // start at global current line

    while (*s) {
        if (*s == '\n') {
            cur_y += 10;        // move down one line
            cur_x = x;          // reset x to start of line
        } else {
            draw_char(fb, fb_width, cur_x, cur_y, *s, color);
            cur_x += 8;         // move to next character
        }
        s++;
    }

    curX = cur_x;
    // update global line counter
    curLine = cur_y;
}

void newLineFB() {
    draw_string(fb, fb_width, 0, curLine, "\n", 0xFFFFFFFF);
}

void draw_dec(unsigned int num) {
    if (num == 0) {
        draw_char(fb, fb_width, curX, curLine, '0', 0xFFFFFFFF);
        return;
    }

    char buffer[10]; // Enough for 32-bit integer decimal digits
    int i = 0;

    // Extract digits in reverse order
    while (num > 0) {
        buffer[i++] = (num % 10) + '0';
        num /= 10;
    }

    // Print digits in correct order
    for (int j = i - 1; j >= 0; j--) {
        draw_char(fb, fb_width, curX, curLine, buffer[j], 0xFFFFFFFF);
    }
}

void draw_hex(uint32_t val) {
    const char *hex = "0123456789ABCDEF";
    char buf[9]; // 8 hex digits + null terminator
    buf[8] = '\0';

    for (int i = 7; i >= 0; i--) {
        buf[i] = hex[val & 0xF];
        val >>= 4;
    }

    draw_string(fb, fb_width, curX, curLine, buf, colorWhite);
}

void backspaceFB(uint32_t *fb, int fb_width, uint32_t bg_color) {
    if (curX >= CHAR_W) {
        curX -= CHAR_W;   // move left one char (in pixels)

        // clear the character cell
        for (int y = 0; y < CHAR_H; y++) {
            for (int x = 0; x < CHAR_W; x++) {
                fb[(curLine + y) * fb_width + (curX + x)] = bg_color;
            }
        }
    } else {
        // at beginning of line -> do nothing
        return;
    }
}

void draw_cursor(uint32_t *fb, int fb_width, uint32_t color) {
    int cursor_y = curLine + CHAR_H - 2; // 2 pixels high
    for (int x = 0; x < CHAR_W; x++) {
        fb[cursor_y * fb_width + (curX + x)] = color; // solid color
    }
}

void erase_cursor(uint32_t *fb, int fb_width, uint32_t bg_color) {
    int cursor_y = curLine + CHAR_H - 2;
    for (int x = 0; x < CHAR_W; x++) {
        fb[cursor_y * fb_width + (curX + x)] = bg_color; // replace with bg
    }
}

void clear(uint32_t color) {
    if (!fb || fb_width == 0 || fb_height == 0) return;

    for (int i = 0; i < fb_width * fb_height; i++) {
        fb[i] = color;
    }

    // Reset text cursor state
    cursor_x = 0;
    cursor_y = 0;
    curLine  = 0;
    curX     = 0;

    // If you want the cursor visible immediately
    if (cursor_visible) {
        draw_cursor(fb, fb_width, colorWhite);
    }
    draw_char(fb, fb_width, 10, 10, ' ', 0xFFFFFFFF);
}

void scroll_down(uint32_t bg_color) {
    if (!fb || fb_width == 0 || fb_height == 0) return;

    int pitch = fb_width;          // pixels per row
    int rows_to_move = fb_height - CHAR_H;

    // Copy each row up by CHAR_H rows
    for (int y = 0; y < rows_to_move; y++) {
        for (int x = 0; x < pitch; x++) {
            fb[y * pitch + x] = fb[(y + CHAR_H) * pitch + x];
        }
    }

    // Clear the bottom CHAR_H rows
    for (int y = rows_to_move; y < fb_height; y++) {
        for (int x = 0; x < pitch; x++) {
            fb[y * pitch + x] = bg_color;
        }
    }

    // Move cursor to the bottom visible row
    curLine = fb_height - CHAR_H;
    curX = 0;
}